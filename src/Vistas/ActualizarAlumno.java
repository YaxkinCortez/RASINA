    void cambiaDatos() {
        int fila = tb.getSelectedRow();
        ID.setText(tb.getValueAt(fila, 0).toString());
        nombre.setText(tb.getValueAt(fila, 1).toString());
    }

    boolean selecionRegistro = false;

    void limpiaCampos() {
        nombre.setText("");
        ID.setText("");
        selecionRegistro = false;
        CAlumno.llenarTabla();
    }

