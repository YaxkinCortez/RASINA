    private void actualizar() {
        if (nombre.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Seleccione un usuarios de la tabla", "ERROR", JOptionPane.ERROR_MESSAGE);
        } else {
            ma.setNombre(nombre.getText());
            ma.setID(ID.getText());
            int op = CAlumno.actualizarFull(ma);
            if (op != 0) {
                limpiaCampos();
                JOptionPane.showMessageDialog(this, "Actualizacion Ã©xitosa.", "ACTUALIZADO", 0,
                        new ImageIcon(getClass().getResource("/img/realizado.png")));
            }
        }
    }

