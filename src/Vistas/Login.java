    private void ingresa() {
        String Usuario = this.txtNombre.getText();
        String Pass = this.txtContra.getText();
        if (Usuario.equals("") && Pass.equals("")) {
            JOptionPane.showMessageDialog(this, "Campos requeridos");
        } else {
            acceso(Usuario, Pass);
        }
    }

    private void acceso(String u, String p) {
        String dato = null;
        try {
            String sql = "SELECT nombre FROM maestros WHERE nombre = '" + u + "'";
            Statement st = cn.createStatement();
            ResultSet rs = st.executeQuery(sql);
            if (rs.first()) {
                String sql1 = "SELECT contra FROM maestros WHERE contra = '" + p + "'";
                Statement st1 = cn.createStatement();
                ResultSet rs1 = st1.executeQuery(sql1);
                if (rs1.first()) {
                    String sql2 = "SELECT tipo FROM maestros WHERE nombre = '" + u + "'"
                            + " and contra = '" + p + "'";
                    Statement st2 = cn.createStatement();
                    ResultSet rs2 = st2.executeQuery(sql2);
                    while (rs2.next()) {
                        dato = rs2.getString(1);
                    }

                    if (dato.equals("1")) {

                        String sql3 = "SELECT nombre FROM maestros WHERE nombre = '" + u + "'";
                        Statement st3 = cn.createStatement();
                        ResultSet rs3 = st3.executeQuery(sql3);
                        while (rs3.next()) {
                            dato = rs3.getString(1);
                        }
                        dispose();
                        Dashboard mp = new Dashboard();
                        JOptionPane.showMessageDialog(this, "BIENVENIDO " + dato, "Bienvenido", JOptionPane.INFORMATION_MESSAGE);
                        //mp.UserConect.setText(dato);
                        mp.setVisible(true);
                        mp.setSize(1090, 710);
                        mp.setLocationRelativeTo(null);
                    } else {
                        String sql3 = "SELECT nombre FROM maestros WHERE nombre = '" + u + "'";
                        Statement st3 = cn.createStatement();
                        ResultSet rs3 = st3.executeQuery(sql3);
                        while (rs3.next()) {
                            dato = rs3.getString(1);
                        }
                        dispose();
                        Dashboard2 mp2 = new Dashboard2();
                        JOptionPane.showMessageDialog(this, "BIENVENIDO " + dato, "Bienvenido", JOptionPane.INFORMATION_MESSAGE);
                        //mp2.UserConect.setText(dato);
                        mp2.setVisible(true);
                        mp2.setLocationRelativeTo(null);
                    }
                } else {
                    JOptionPane.showMessageDialog(this, "Contrase√±a incorrecta", "Acceso", 0);
                    this.txtContra.setText("");
                    this.txtNombre.transferFocus();
                }
            } else {
                JOptionPane.showMessageDialog(this, "No existe √©ste usuario.", "Acceso", 0);
                this.txtContra.setText("");
                this.txtNombre.transferFocus();
            }
        } catch (SQLException ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

